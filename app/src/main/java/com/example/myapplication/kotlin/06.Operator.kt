// 06. Operator -> 연산자

// 1. 산술연산자
// +, - . *. /(몫만 취함), %(나머지만 취함)

// 2. 대입연산자
// 좌변 = 우변 (우변값이 좌변에 들어간다)
// a = 5 (o), 5 = a (x)

// 3. 복합대입연산자
// += , -=, *= , /= , %=
// ex) a += 10 -> a = a + 10

// 4. 증감연산자
// ++, --
// ex) a++, a--

// 5. 비교연산자
// >=, <=, >, < , ==, !=
// True == True -> True
// True == False -> False
// True != True -> False
// True != False -> True

// 6. 논리연산자
// &&, ||, !
// True && True -> True
// True || False -> True
// !True -> False
// !False -> True
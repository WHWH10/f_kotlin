// 핸드폰마다 화면 크기가 다 다른데 어떻게 그려야 할까
// - 픽셀, dpi, dp
// - 픽셀 : 핸드폰 화면에서 빛이 나오는 전구 -> 가장 작은 단위
// - dpi : dot per inch
//   - ldpi : 120 (1인치에 120픽셀)
//   - mdpi : 160 (1인치에 240픽셀)
//   - hdpi : 240
//   - xhdpi : 320
//   - xxhdpi : 480
//   - xxxhdpi : 640
// - dp : Density Independent Pixel
//   - 픽셀 독립적인 단위 ( 핸드폰 해상도 상관없이 동일하게 나옴)
